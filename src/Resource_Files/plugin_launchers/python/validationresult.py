class ValidationResult(object):

    TYPE_INFO  = 'info'
    TYPE_WARN  = 'warning'
    TYPE_ERROR = 'error'

    def __init__(self, restype, filename, linenumber, message):
        self.restype = restype
        self.filename = filename
        self.linenumber = linenumber
        self.message = message
